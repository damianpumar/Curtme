<script>
  import { onMount, onDestroy } from "svelte";
  import {
    validURL,
    getSourceURLFromParameter,
    clearLinkParameter,
  } from "../utils/url";
  import { isEnterKeyDown } from "../utils/keyboard";
  import { gaEventUserCreateShortLink } from "../utils/ga.js";
  import {
    createLink,
    getLinks,
    getUserLinks,
    syncLinksWithUser,
  } from "../utils/api";
  import { parseDate } from "../utils/date";
  import {
    NAME,
    TITLE,
    SHORT,
    PASTE_LONG_URL,
    INTERNET_CONNECTION,
    URL_INVALID,
    URL_MANDATORY,
  } from "../utils/resources.js";
  import { isAuthenticated, isLoading } from "../auth0/auth0.store";

  import Error from "./Error.svelte";
  import Link from "./Link.svelte";

  const STORAGE_KEY = "links";

  let longInputElement;
  let sourceURL = null;
  let errorMessage = null;
  let links = [];
  $: orderedLinks = links.sort(
    (l1, l2) => parseDate(l2.date) - parseDate(l1.date)
  );

  async function loadLinksWithoutUser() {
    const linksStored = localStorage.getItem(STORAGE_KEY);

    if (linksStored) {
      const linkStoredParsed = JSON.parse(linksStored);

      const response = await getLinks(linkStoredParsed);

      if (response.ok) {
        links = await response.json();

        localStorage.setItem(STORAGE_KEY, JSON.stringify(links));
      }
    }
  }

  async function loadLinkWithUser() {
    const response = await getUserLinks();

    if (response.ok) {
      links = await response.json();
    }
  }

  async function syncLinkWithoutUser() {
    const linksStored = localStorage.getItem(STORAGE_KEY);

    if (linksStored) {
      const linkStoredParsed = JSON.parse(linksStored);

      const response = await syncLinksWithUser(linkStoredParsed);

      if (response.ok) {
        localStorage.removeItem(STORAGE_KEY);
      } else {
        await loadLinksWithoutUser();
      }
    }
  }

  function addLink(link) {
    links = [...links, link];

    if (!$isAuthenticated) {
      localStorage.setItem(STORAGE_KEY, JSON.stringify(links));
    }
  }

  async function createShortURL() {
    if (!sourceURL) {
      errorMessage = URL_MANDATORY;
      return;
    }

    if (!validURL(sourceURL)) {
      errorMessage = URL_INVALID;
      return;
    }

    try {
      gaEventUserCreateShortLink();

      const response = await createLink(sourceURL);

      if (response.ok) {
        const link = await response.json();

        sourceURL = null;

        addLink(link);
      } else {
        errorMessage = URL_INVALID;
      }
    } catch (exception) {
      errorMessage = INTERNET_CONNECTION;
    }
  }

  async function loadLinks() {
    if ($isAuthenticated) {
      await syncLinkWithoutUser();
      loadLinkWithUser();
    } else {
      loadLinksWithoutUser();
    }
  }

  const unsubscribe = isLoading.subscribe((newValue) => {
    if (!newValue) {
      loadLinks();
    }
  });

  onMount(() => {
    sourceURL = getSourceURLFromParameter();

    if (sourceURL) {
      clearLinkParameter();
      createShortURL();
    } else {
      longInputElement.focus();
    }
  });

  onDestroy(unsubscribe);
</script>

<section class="container medium">
  <h2>{NAME}</h2>
  <p>{TITLE}</p>
  <div class="row">
    <div class="col-12 col-12-mobilep">
      <input
        type="text"
        autocomplete="false"
        bind:value={sourceURL}
        placeholder={PASTE_LONG_URL}
        bind:this={longInputElement}
        on:keydown={(event) => isEnterKeyDown(event) && createShortURL()}
      />
    </div>
    <Error bind:error={errorMessage} />
  </div>
  <div class="row">
    <div class="col-12 col-12-mobilep">
      <button on:click={createShortURL}>{SHORT}</button>
    </div>
  </div>
</section>

{#each orderedLinks as link}
  <Link {link} />
{/each}

